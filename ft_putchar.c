#include "printf.h"

void		ft_putchar(wchar_t c)
{
	unsigned char	ch[4];

	if (c <= _7_BITS || MB_CUR_MAX != 4)
		ch[0] = c;
	else if (c <= _11_BITS)
	{
		ch[0] = (c >> 6 & _00011111) | _11000000;
		ch[1] = (c & _00111111) | _10000000;
	}
	else if (c <= _16_BITS)
	{
		ch[0] = (c >> 12 & _00001111) | _11100000;
		ch[1] = (c >> 6 & _00111111) | _10000000;
		ch[2] = (c & _00111111) | _10000000;
	}
	else
	{
		ch[0] = (c >> 18 & _00000111) | _11110000;
		ch[1] = (c >> 12 & _00111111) | _10000000;
		ch[2] = (c >> 6 & _00111111) | _10000000;
		ch[3] = (c & _00111111) | _10000000;
	}
	write(1, &ch, size_of(c));
}

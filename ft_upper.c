void	ft_upper(char **str)
{
	int i;

	i = 0;
	while (*str && (*str)[i])
	{
		if ((*str)[i] >= 'a' && (*str)[i] <= 'z')
			(*str)[i] = (*str)[i] - 32;
		i++;
	}
}
